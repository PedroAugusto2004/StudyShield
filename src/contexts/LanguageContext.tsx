const detectBrowserLanguage = (): Language => {
  try {
    const browserLang = navigator.language.toLowerCase();
    console.log('Detected browser language:', browserLang);
    
    if (browserLang.startsWith('es')) return 'es';
    if (browserLang.startsWith('fr')) return 'fr';
    if (browserLang.startsWith('de')) return 'de';
    if (browserLang.startsWith('pt')) return 'pt';
    if (browserLang.startsWith('zh')) return 'zh';
    if (browserLang.startsWith('ja')) return 'ja';
    if (browserLang.startsWith('ko')) return 'ko';
    
    return 'en'; // Default to English
  } catch (error) {
    console.error('Error detecting browser language:', error);
    return 'en';
  }
};

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const { user } = useAuth();
  const [language, setLanguageState] = useState<Language>(() => {
    // First check localStorage, then browser language
    const saved = localStorage.getItem('userLanguage') as Language;
    if (saved && ['en', 'es', 'fr', 'de', 'pt', 'zh', 'ja', 'ko'].includes(saved)) {
      console.log('Using saved language:', saved);
      return saved;
    }
    
    const detected = detectBrowserLanguage();
    console.log('Using detected browser language:', detected);
    return detected;
  });
  const [isInitialized, setIsInitialized] = useState(false);

  const setLanguage = async (lang: Language) => {
    console.log('Setting language to:', lang);
    setLanguageState(lang);
    localStorage.setItem('userLanguage', lang);
    
    // Sync to database if user is logged in
    if (user?.id) {
      try {
        const success = await userDataService.updateLanguagePreference(user.id, lang);
        if (success) {
          console.log('Language preference synced to database successfully');
        } else {
          console.warn('Failed to sync language preference to database');
        }
      } catch (error) {
        console.error('Failed to sync language preference:', error);
      }
    }
  };

  const t = (key: string): string => {
    return translations[language]?.[key] || translations.en[key] || key;
  };

  // Load language preference from database when user logs in
  useEffect(() => {
    const loadLanguagePreference = async () => {
      if (user?.id && !isInitialized) {
        try {
          console.log('Loading language preference from database for user:', user.id);
          const dbLanguage = await userDataService.getLanguagePreference(user.id);
          
          if (dbLanguage && ['en', 'es', 'fr', 'de', 'pt', 'zh', 'ja', 'ko'].includes(dbLanguage)) {
            console.log('Found database language preference:', dbLanguage);
            if (dbLanguage !== language) {
              setLanguageState(dbLanguage as Language);
              localStorage.setItem('userLanguage', dbLanguage);
            }
          } else {
            // No database preference found, save current language to database
            console.log('No database language preference found, saving current language:', language);
            await userDataService.updateLanguagePreference(user.id, language);
          }
          setIsInitialized(true);
        } catch (error) {
          console.error('Failed to load language preference:', error);
          setIsInitialized(true);
        }
      }
    };

    loadLanguagePreference();
  }, [user?.id, language, isInitialized]);

  // Listen for language updates from other parts of the app
  useEffect(() => {
    const handleLanguageUpdate = (event: CustomEvent) => {
      const { language: newLanguage } = event.detail;
      if (newLanguage && newLanguage !== language) {
        setLanguageState(newLanguage as Language);
      }
    };

    window.addEventListener('languageUpdated', handleLanguageUpdate as EventListener);
    return () => {
      window.removeEventListener('languageUpdated', handleLanguageUpdate as EventListener);
    };
  }, [language]);

  // Auto-detect and save language on first visit (when no user and no saved preference)
  useEffect(() => {
    const savedLanguage = localStorage.getItem('userLanguage');
    if (!savedLanguage && !user) {
      const detected = detectBrowserLanguage();
      console.log('First visit - setting language to:', detected);
      setLanguageState(detected);
      localStorage.setItem('userLanguage', detected);
    }
  }, [user]);

  // Reset initialization flag when user changes
  useEffect(() => {
    if (!user) {
      setIsInitialized(false);
    }
  }, [user]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};